                    làm rò rỉ thông tin trực tuyến, các nhà báo có mối quan hệ mua bán với những người có thông tin, và bất
                    cứ điều gì mà anh nghĩ rằng Apple sẽ thấy thú vị và đáng để tiến hành điều tra.</p>
                <p class="fs-5 mb-4">Nhưng gần đây, điệp viên "hai mang" này đã quyết định chia sẻ câu chuyện của mình
                    vì anh cảm thấy rằng Apple đã lợi dụng mình và đáng lẽ phải bồi thường cho anh vì đã cung cấp các thông
                    tin này cho công ty.</p>
                <p class="fs-5 mb-4">"Tôi làm điều này chủ yếu vì tôi nhận ra rằng trong mối quan hệ đó, Apple chưa bao
                    giờ coi trọng tôi và đối xử với tôi như một con người", Shumeyko cho biết.</p>
                <p class="fs-5 mb-4">Anh đã chia sẻ một số bằng chứng bao gồm các văn bản và một chuỗi email giữa anh
                    và một địa chỉ email trong nhóm Bảo mật toàn cầu của Apple. Tiêu đề của chúng cho thấy Shumeyko đã nhận
                    được email phản hồi từ chính các máy chủ do Apple sở hữus.</p>
