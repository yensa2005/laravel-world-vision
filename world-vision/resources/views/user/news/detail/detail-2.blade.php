                <p class="fs-5 mb-4">Shumeyko cho biết anh đã thiết lập mối quan hệ với nhóm chống rò rỉ của Apple -
                    đơn vị có tên chính thức là Global Security - sau khi anh cảnh báo họ về một chiến dịch lừa đảo chống
                    lại một số nhân viên Apple Store vào năm 2017. Sau đó, vào giữa năm 2020, anh đã cố gắng giúp Apple điều
                    tra một trong những vụ rò rỉ thông tin được cho là "tồi tệ nhất".</p>
                <p class="fs-5 mb-4">Sự việc đó xảy ra vài tháng trước khi hệ điều hành di động của Apple, iOS 14, được
                    phát hành chính thức. Các hacker đã có trong tay một phiên bản rò rỉ của hệ điều hành đầu tiên này. Vào
                    thời điểm đó, những người trong cộng đồng hack iPhone nói rằng bản dựng iOS bị rò rỉ đến từ một nguyên
                    mẫu bị đánh cắp của iPhone 11, thứ được mua từ các nhà cung cấp ở một chợ xám tại Trung Quốc. Từ lâu,
                    phần mềm và phần cứng của Apple thỉnh thoảng bị rò rỉ ra khỏi Trung Quốc, nơi đặt hầu hết các dây chuyền
                    lắp ráp iPhone, và có một thị trường chợ xám đã phát triển mạnh chuyên về các nguyên mẫu iPhone bị đánh
                    cắp.</p>
