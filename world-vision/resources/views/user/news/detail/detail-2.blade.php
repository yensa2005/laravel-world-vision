                <p class="fs-5 mb-4">Trên Twitter và trong các kênh Discord dành cho cộng đồng, anh quảng cáo các ứng
                    dụng bị rò rỉ, hướng dẫn sử dụng và bán các nguyên mẫu thiết bị bị đánh cắp. Nhưng các thành viên khác
