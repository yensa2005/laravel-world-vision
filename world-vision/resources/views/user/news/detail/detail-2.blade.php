                    hàng hóa đó, hoặc ngoài ý muốn của cơ quan nhà nước điều tiết thị trường. Các sản phẩm này được tiếp thị
                    cho các nhà nghiên cứu bảo mật và tin tặc, những người quan tâm đến việc tìm kiếm lỗ hổng và phát triển
                    phần mềm khai thác cho các thiết bị của Apple.</p>
                <p class="fs-5 mb-4">Apple rõ ràng không hài lòng về bất kỳ điều gì trong số này. Nhưng trong những năm
                    qua, công ty phần lớn vẫn giữ nguyên phản ứng không bình luận gì trước những thông tin rò rỉ. Vào giữa
                    tháng 6 năm nay, các luật sư của Apple tại Trung Quốc đã gửi thư cho một công dân Trung Quốc, người đã
                    quảng cáo và bán các thiết bị bị đánh cắp , yêu cầu họ ngừng hoạt động và tiết lộ nguồn tin bên trong
                    công ty, như Motherboard đã đưa tin vào tháng trước.

                    Các nhân viên trong nhóm Global Security được cho là các cựu nhân viên FBI và tình báo Mỹ. Những người
                    này được giao nhiệm vụ truy quét các vụ rò rỉ, nhưng rất ít thông tin về cách thức hoạt động của tổ chức
                    này được hé lộ.

                    Và một trong những cách mà nhóm này theo dõi các thông tin bị rò rỉ là vun đắp mối quan hệ với những
                    người trong cộng đồng jailbreak iOS, chẳng hạn như Shumeyko. Đây không phải là lần đầu tiên chuyện như
                    thế này xảy ra. Vào năm 2017, một nhân viên của Apple cũng đã tự mình thâm nhập vào cộng đồng này, sau
                    đó hoạt động như một điệp viên hai mang.</p>
